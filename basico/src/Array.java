
public class Array {
    public static void main(String[] args) {
        // vetor de 4 posicoes
        int vet[] = new int[4];

        // declaro ja sabendo os valores
        int vet2[] = {5, 9, 13, 1};

        // foreach - para cada valor
        for (int valor : vet2) {
            System.out.print(valor + " ");
        }
    }
}
